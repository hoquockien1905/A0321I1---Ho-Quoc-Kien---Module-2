package bai_10_dsa_danh_sach.bai_tap.trien_khai_cac_phuong_thuc_cua_linked_list;

public class Node {
    public Node next;
    public Object data;

    public boolean indexData(int index) {
        return this.indexData(index);
    }

    public Node(Object data) {
        this.data = data;
        this.next = null;
    }

    public Object getData() {
        return data;
    }
}
